CREATE TABLE `Capital` (
  `capital` varchar(20) NOT NULL,

  PRIMARY KEY (`capital`)
);

CREATE TABLE `Registration` (
  `uuid`         varchar(36)   NOT NULL,
  `email`        varchar(100)  NOT NULL,
  `login`        varchar(20)   NOT NULL,
  `password`     varchar(32)   NOT NULL,
  `role`         varchar(10)   NOT NULL,
  `completeName` varchar(30)   DEFAULT NULL,
  `description`  varchar(1000) DEFAULT NULL,
  `fbUrl`        varchar(50)   DEFAULT NULL,
  `twURL`        varchar(50)   DEFAULT NULL,
  `personalUrl`  varchar(50)   DEFAULT NULL,
  `image`        longblob      DEFAULT NULL,

  PRIMARY KEY (`uuid`),
  UNIQUE KEY `UK_Registration_email` (`email`),
  UNIQUE KEY `UK_Registration_login` (`login`)
);

CREATE TABLE `User` (
  `login`         varchar(20)   NOT NULL,
  `email`         varchar(100)  NOT NULL,
  `password`      varchar(32)   NOT NULL,
  `role`          varchar(10)   NOT NULL,
  `completeName`  varchar(30)   DEFAULT NULL,
  `description`   varchar(1000) DEFAULT NULL,
  `fbUrl`         varchar(50)   DEFAULT NULL,
  `twURL`         varchar(50)   DEFAULT NULL,
  `personalUrl`   varchar(50)   DEFAULT NULL,
  `notifications` tinyint  	    NOT NULL,
  `image`         longblob      DEFAULT NULL,
  `city`          varchar(20)   ,

  PRIMARY KEY (`login`),
  UNIQUE KEY `UK_User_email` (`email`)
);

CREATE TABLE `Event` (
  `id`          int(11)      NOT NULL AUTO_INCREMENT,
  `date`        timestamp    NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `category`    varchar(255) NOT NULL,
  `location`    varchar(100) NOT NULL,
  `summary`     varchar(50)  NOT NULL,
  `title`       varchar(20)  NOT NULL,
  `owner`       varchar(20)  NOT NULL,
  `cancelled`   tinyint  	 NOT NULL,
  `description` varchar(1000),
  `place`       varchar(20),

  PRIMARY KEY (`id`),
  KEY `K_Event_Owner` (`owner`),
  KEY `K_Event_Capital` (`place`),

  CONSTRAINT `FK_Event_Owner` FOREIGN KEY (`owner`) REFERENCES `User` (`login`),
  CONSTRAINT `FK_Event_Capital` FOREIGN KEY (`place`) REFERENCES `Capital` (`capital`)
);

CREATE TABLE `EventAttendees` (
  `user_login` varchar(20) NOT NULL,
  `event_id`   int(11)     NOT NULL,

  KEY `K_UserJoinsEvent_user_login` (`user_login`),
  KEY `K_UserJoinsEvent_event_id`   (`event_id`),

  CONSTRAINT `FK_UserJoinsEvent_user_login` FOREIGN KEY (`user_login`) REFERENCES `User`  (`login`),
  CONSTRAINT `FK_UserJoinsEvent_event_id`   FOREIGN KEY (`event_id`)   REFERENCES `Event` (`id`)
);

CREATE TABLE `CapitalDistances` (
  `capital_A` varchar(20) NOT NULL,
  `capital_B` varchar(20) NOT NULL,
  `distance`  int(11)     NOT NULL,

  KEY `K_CapitalDistances_capital_A` (`capital_A`),
  KEY `K_CapitalDistances_capital_B` (`capital_B`),

  CONSTRAINT `FK_CapitalDistances_capital_A` FOREIGN KEY (`capital_A`) REFERENCES `Capital` (`capital`),
  CONSTRAINT `FK_CapitalDistances_capital_B` FOREIGN KEY (`capital_B`) REFERENCES `Capital` (`capital`)
);


CREATE TABLE `Notification` (
  `id` int NOT NULL AUTO_INCREMENT,
  `title` varchar(100) NOT NULL,
  `body` varchar(1000) NOT NULL,

  PRIMARY KEY (`id`)
);


CREATE TABLE `UserNotifications` (
  `userId` varchar(20) NOT NULL,
  `notificationId` int NOT NULL,
  `readed` tinyint NOT NULL,

  KEY `K_UserNotifications_userId`(`userId`),
  KEY `K_UserNotifications_notificationId`	(`notificationId`),

  CONSTRAINT `fk_UserNotifications_1`  FOREIGN KEY (`userId`)  REFERENCES `User` (`login`),
  CONSTRAINT `fk_UserNotifications_2`   FOREIGN KEY (`notificationId`) REFERENCES `Notification` (`id`)
);

